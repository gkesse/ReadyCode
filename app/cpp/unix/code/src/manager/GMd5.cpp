#include "GMd5.h"
//===============================================
GMd5::GMd5() : GObject() {
    m_data = "";
}
//===============================================
GMd5::GMd5(const std::string& _data) : GObject() {
    m_data = _data;
}
//===============================================
GMd5::~GMd5() {

}
//===============================================
std::string GMd5::encodeData() const {
    if(m_data == "") return "";
    MD5 lMd5;
    std::string lData = lMd5(m_data);
    return lData;
}
