#include "GPath.h"
//===============================================
GApp* GApp::m_instance = 0;
//===============================================
GApp::GApp()
: GCode() {

}
//===============================================
GApp::~GApp() {

}
//===============================================
GApp* GApp::Instance() {
    if(m_instance == 0) {
        m_instance = new GApp;
        m_instance->loadFile(GPATH("xml", "app.xml"));
    }
    return m_instance;
}
