//===============================================
#include "GLine.h"
//===============================================
GLine::GLine() {
    m_x1 = 0.f;
    m_y1 = 0.f;
    m_x2 = 0.f;
    m_y2 = 0.f;
    m_a = 0.f;
    m_b = 0.f;
}
//===============================================
GLine::~GLine() {

}
//===============================================
GLine::GLine(float _x1, float _y1, float _x2, float _y2) {
    m_x1 = _x1;
    m_y1 = _y1;
    m_x2 = _x2;
    m_y2 = _y2;
    m_a = 0.f;
    m_b = 0.f;
    computeA();
    computeB();
}
//===============================================
void GLine::computeA() {
    m_a = (m_y2 - m_y1) / (m_x2 - m_x1);
}
//===============================================
void GLine::computeB() {
    m_b = m_y1 - m_a * m_x1;
}
//===============================================
float GLine::valX(float _y) {
    float lX = (_y - m_b) / m_a;
    return lX;
}
//===============================================
float GLine::valY(float _x) {
    float lY = m_a * _x + m_b;
    return lY;
}
