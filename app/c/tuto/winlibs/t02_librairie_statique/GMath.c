#include "GMath.h"
//===============================================
double GMath_add(double a, double b) {
    return a + b;
}
//===============================================
double GMath_sub(double a, double b) {
    return a - b;
}
//===============================================
double GMath_mul(double a, double b) {
    return a * b;
}
//===============================================
double GMath_div(double a, double b) {
    return a / b;
}
//===============================================
int GMath_quo(int a, int b) {
    return a / b;
}
//===============================================
int GMath_mod(int a, int b) {
    return a % b;
}
