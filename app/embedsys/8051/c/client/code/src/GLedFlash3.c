//===============================================
#include "GLedFlash3.h"
//===============================================
#define DATA_PORT   P1
//===============================================
sbit DATA_PIN       = DATA_PORT^0;
//===============================================
static uchar gTime = 0;
//===============================================
void GLedFlash3_init() {
    DATA_PORT = 0xFF;
}
//===============================================
void GLedFlash3_update() {
    if(++gTime >= 50) {
        DATA_PIN = !DATA_PIN;
        gTime = 0;
    }
}
